         II=NINT(READA(KEYWRD,INDEX(KEYWRD,'HESS=')))
         IF (II.EQ.0) WRITE(6,130)
         IF (II.EQ.1) WRITE(6,140)
         IF (II.EQ.2) WRITE(6,150)
         IF (II.EQ.3) WRITE(6,160)
